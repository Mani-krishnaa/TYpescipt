var str = "Hello, World!";
console.log(str);
console.log("String length: ".concat(str.length));
console.log("First character: ".concat(str.charAt(0)));
console.log("Substring (0, 5): ".concat(str.substring(0, 5)));
console.log("Uppercase: ".concat(str.toUpperCase()));
console.log("Lowercase: ".concat(str.toLowerCase()));
console.log("Contains 'World': ".concat(str.includes("World")));
